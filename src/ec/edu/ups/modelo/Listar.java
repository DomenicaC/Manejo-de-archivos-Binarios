    public void llenarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tblPer.getModel();
        Set<Persona> lista = contPer.getLista();
        for (Persona per : lista) {
            Object[] datos = {
                per.getNombre(),
                per.getApellido(),
                per.getCedula(),
                per.getCelular(),
                per.getEdad(),
                per.getFechaNac(),
                per.getSueldo()};

            modelo.addRow(datos);
        }
    }

